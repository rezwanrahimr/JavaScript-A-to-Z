var date = new Date();
console.log(date);

// get month.
var month = date.getMonth();
console.log(month);
// get year.
var year = date.getFullYear();
console.log(year + " year");
// get day
var day = date.getDay();
console.log(day);
// get current time
var time = date.getHours();
console.log(time);
